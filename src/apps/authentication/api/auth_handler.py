import time
from typing import Dict

import jwt
from config import settings


JWT_SECRET = settings.authentication.secret_key
JWT_ALGORITHM = settings.authentication.algorithm
JWT_ACCESS_TOKEN_EXPIRE_MINUTES = settings.authentication.access_token_expire_minutes


def token_response(token: str):
    return {
        "access_token": token
    }


def signJWT(user_id: str) -> Dict[str, str]:
    payload = {
        "user_id": user_id,
        "expires": time.time() + JWT_ACCESS_TOKEN_EXPIRE_MINUTES
    }
    token = jwt.encode(payload, JWT_SECRET, algorithm=JWT_ALGORITHM)

    return token_response(token)


def decodeJWT(token: str) -> dict:
    try:
        decoded_token = jwt.decode(token, JWT_SECRET, algorithms=[JWT_ALGORITHM])
        return decoded_token if decoded_token["expires"] >= time.time() else None
    except:
        return {}
